namespace SouthAfricanId.Models;


/// <summary>
/// Represents gender information as encoded in a South African ID number.
/// </summary>
public enum Gender
{
    /// <summary>
    /// Male gender.
    /// </summary>
    Male,
    /// <summary>
    /// Female gender.
    /// </summary>
    Female,
    /// <summary>
    /// Other or unknown gender.
    /// </summary>
    Other
}
